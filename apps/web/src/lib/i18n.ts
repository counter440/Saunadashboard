export type TKey = keyof typeof dict.nb;

/** Translate a key into the given locale, with optional `{name}` interpolation. */
export function tFor(locale: Locale, key: TKey, vars?: Record<string, string | number>): string {
	let s = (dict[locale][key] ?? dict.en[key] ?? key) as string;
	if (vars) {
		for (const [name, val] of Object.entries(vars)) {
			s = s.replace(new RegExp(`\\{${name}\\}`, "g"), String(val));
		}
	}
	return s;
}

export function isLocale(v: unknown): v is Locale {
	return v === "nb" || v === "en";
}

/** Pluralize using en/nb rules (1 vs other). Pass `{n}` in the templates. */
export function plural(locale: Locale, n: number, oneKey: TKey, otherKey: TKey): string {
	return tFor(locale, n === 1 ? oneKey : otherKey, { n });
}

/** Locale-aware "X minutes ago" formatter. */
export function relativeFromNowI18n(locale: Locale, d: Date | string | null | undefined): string {
	if (!d) return tFor(locale, "common.never");
	const date = typeof d === "string" ? new Date(d) : d;
	const ms = Date.now() - date.getTime();
	const min = Math.round(ms / 60_000);
	if (min < 1) return tFor(locale, "common.justNow");
	if (min < 60) return tFor(locale, "common.minAgo", { n: min });
	const h = Math.round(min / 60);
	if (h < 24) return tFor(locale, "common.hAgo", { n: h });
	const day = Math.round(h / 24);
	return tFor(locale, "common.dAgo", { n: day });
}
